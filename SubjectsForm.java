    private void subjectTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_subjectTableMouseClicked

    DefaultTableModel subjtable = (DefaultTableModel) subjectTable.getModel();
    int selectedRow = subjectTable.getSelectedRow();
    if (selectedRow == -1) return;

    subjId.setText(subjtable.getValueAt(selectedRow, 0).toString());
    subjCode.setText(subjtable.getValueAt(selectedRow, 1).toString());
    subjDesc.setText(subjtable.getValueAt(selectedRow, 2).toString());
    subjUnits.setText(subjtable.getValueAt(selectedRow, 3).toString());
    sched.setText(subjtable.getValueAt(selectedRow, 4).toString());

    try {
        int subjid = Integer.parseInt(subjId.getText().trim());
        showClassList(subjid);
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Invalid subject ID.");
    }
    }//GEN-LAST:event_subjectTableMouseClicked

