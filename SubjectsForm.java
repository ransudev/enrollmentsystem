    public void showrec(){
        
        DefaultTableModel subjtable = (DefaultTableModel) subjectTable.getModel();
        subjtable.setRowCount(0);
        
        MaisoEnrollmentSystem a = new MaisoEnrollmentSystem();
        a.DBConnect();
        try{
            String query = "select * from Subjects;";
                a.rs = a.st.executeQuery(query);
                while (a.rs.next()){
                    String subjId = a.rs.getString("subjid");
                    String subjCode = a.rs.getString("subjcode");
                    String subjDesc = a.rs.getString("subjdesc");
                    String subjUnits = a.rs.getString("subjunits");
                    String sched = a.rs.getString("subjsched");
                  
                    String[] item = {subjId, subjCode, subjDesc, subjUnits, sched};
                    subjtable.addRow(item);
                }
        }catch (Exception ex) {
            System.out.println("FAILED" + ex);
        }
    }
    

    public void showClassList(int subjId) {
        DefaultTableModel model = (DefaultTableModel) classlist.getModel();
        model.setRowCount(0); 

        MaisoEnrollmentSystem a = new MaisoEnrollmentSystem();
        a.DBConnect();
        try {
        String sql =
            "SELECT s.studid, s.studname, s.studadd, s.studcontact, s.studgender, s.yrlvl " +
            "FROM Enroll e " +
            "INNER JOIN Students s ON s.studid = e.studid " +
            "WHERE e.subjid = " + subjId + " " +
            "ORDER BY s.studid";
            a.rs = a.st.executeQuery(sql);
            while (a.rs.next()) {
                String[] row = {
                    a.rs.getString("studid"),
                    a.rs.getString("studname"),
                    a.rs.getString("studadd"),
                    a.rs.getString("studcontact"),
                    a.rs.getString("studgender"),
                    a.rs.getString("yrlvl")
                };
                model.addRow(row);
            }
        } catch (Exception ex) {
            System.out.println("showClassList FAILED: " + ex);
        }
    }

